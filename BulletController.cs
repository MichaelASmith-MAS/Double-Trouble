using UnityEngine;
using System.Collections;
using System.Collections.Generic;

public class BulletController : MonoBehaviour 
{
    // ------------------------------------------------------------------------------
    // Public Variables
    // ------------------------------------------------------------------------------

    public float damage = 10f;
	
	// ------------------------------------------------------------------------------
	// Protected Variables
	// ------------------------------------------------------------------------------



	// ------------------------------------------------------------------------------
	// Private Variables
	// ------------------------------------------------------------------------------


	
	// ------------------------------------------------------------------------------
    // GETTERS/SETTERS
    // ------------------------------------------------------------------------------



	// ------------------------------------------------------------------------------
	// FUNCTIONS
	// ------------------------------------------------------------------------------

	// ------------------------------------------------------------------------------
	// Function Name: Start
	// Return types: N/A
	// Argument types: N/A
	// Author: 
	// Date: 
	// ------------------------------------------------------------------------------
	// Purpose: Used to initialize variables or perform startup processes
	// ------------------------------------------------------------------------------
	void OnCollisionEnter (Collision other) 
	{
        if (other.gameObject.tag == Tags.player)
        {
            other.gameObject.GetComponent<PlayerHealth>().Damage(damage);
        }
        else if (other.gameObject.tag == Tags.enemy)
        {
            other.gameObject.GetComponent<EnemyHealth>().Damage(damage);
        }

        Destroy(this.gameObject);

	} //End Start
	
} // End BulletController